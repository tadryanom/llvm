#ifndef TILEINSTPRINTER_H
#define TILEINSTPRINTER_H
#include "TileMCInst.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
class TileInstPrinter : public MCInstPrinter {
public:
  TileInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                  const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);

  virtual void printRegName(raw_ostream &OS, unsigned RegNo) const;
  virtual void printInst(const MCInst *MI, raw_ostream &O, StringRef Annot);
  void printInst(const TileMCInst *MI, raw_ostream &O, StringRef Annot);

private:
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printUnsignedImm(const MCInst *MI, int opNum, raw_ostream &O);
  void printS16ImmOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  bool printPICLNKReg(const MCInst *MI, unsigned opNum, raw_ostream &OS);
};
} // end namespace llvm

#endif
